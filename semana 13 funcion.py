# Esta función maneja datos de temperaturas de multiples cuidades y semanas.
def calcular_temperatura_promedio(datos_ciudades):
    """
    Calcula la temperatura promedio de cada ciudad durante el período dado.

    Args:
        datos_ciudades (list): Una lista de listas, donde cada sublista representa los datos de temperatura de una ciudad.

    Returns:
        dict: Un diccionario donde las claves son los nombres de las ciudades y los valores son las temperaturas promedio correspondientes.
    """

    temperaturas_promedio = {}
    nombres_ciudades = ["ORELLANA", "GUAQUIL", "LOJA"]  # Nombres de las ciudades

    for i, datos_ciudad in enumerate(datos_ciudades):
        total_temperatura = 0
        total_dias = 0

        for semana in datos_ciudad:
            for dia in semana:
                total_temperatura += dia["temp"]
                total_dias += 1

        promedio = total_temperatura / total_dias if total_dias > 0 else 0
        temperaturas_promedio[nombres_ciudades[i]] = promedio

    return temperaturas_promedio

